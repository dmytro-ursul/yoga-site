// i18n engine
(function() {
  const STORAGE_KEY = 'yoga-locale';
  let currentLang = localStorage.getItem(STORAGE_KEY) || 'uk';

  function applyTranslations(lang) {
    currentLang = lang;
    const t = translations[lang];
    if (!t) return;

    // Update <html lang>
    document.documentElement.lang = lang === 'uk' ? 'uk' : lang === 'ru' ? 'ru' : 'en';

    // Update page title and meta
    document.title = t['meta.title'];
    const metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc) metaDesc.setAttribute('content', t['meta.description']);

    // Apply text translations
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (t[key] !== undefined) el.textContent = t[key];
    });

    // Apply HTML translations
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      const key = el.getAttribute('data-i18n-html');
      if (t[key] !== undefined) el.innerHTML = t[key];
    });

    // Highlight active switcher button
    document.querySelectorAll('[data-lang-btn]').forEach(btn => {
      const isActive = btn.getAttribute('data-lang-btn') === lang;
      btn.className = isActive
        ? 'px-1 text-xs font-bold text-white transition-all duration-200 cursor-pointer'
        : 'px-1 text-xs font-normal text-white/50 hover:text-white/80 transition-all duration-200 cursor-pointer';
    });

    localStorage.setItem(STORAGE_KEY, lang);
  }

  // Expose globally
  window.setLang = function(lang) {
    applyTranslations(lang);
  };

  // Apply on load
  document.addEventListener('DOMContentLoaded', () => {
    applyTranslations(currentLang);
  });
})();
